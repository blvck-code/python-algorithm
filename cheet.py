# Variables assignment
x = 5
print(x)

# calculations with variables
x + 2
print(x + 2)

# string
my_string = 'Maurice'
print(my_string * 2)
print('m' in my_string)
print('M' in my_string)

# Lists
a = 'is'
b = 'nice'
my_list = ['my', 'listing', a, b]
my_list2 = [[4, 5, 6, 7], [3, 4, 5, 6]]
print(my_list)
print(my_list2)

# Selecting List Elements
print(my_list[1]) # selecting index 1
print(my_list[3]) # selecting index 3
print(my_list[-3]) # selecting 3rd last item

# Slice
print(my_list[1:3]) # selecting index 1 and 2
print(my_list[1:]) # selecting items from index 0
print(my_list[:3]) # selecting items before index 3
print(my_list[:]) # copy whole list


